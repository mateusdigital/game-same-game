function String_Cat()
{
    let s = "";
    for(let i = 0; i < arguments.length; ++i) {
        s += arguments[i];
    }
    return s;
}

//------------------------------------------------------------------------------
function String_Join(sep, ...args)
{
    let s = "";
    for(let i = 0; i < args.length-1; ++i) {
        s += args[i] + sep;
    }
    s += Array_GetBack(args);

    return s;
}